"""Build the one-document-per-type smoke corpus + its filtered ground truth (SP_015).

Deterministic, no network, no DB — mirrors the verified ``eval/sample/build_sample.py``:
  * copy each manifest doc into ``<dest>/eval/smoke/data/<same subpath>`` so the connector
    glob + ``source_uri`` match the real corpus exactly (run ingest with root ``data`` from
    ``eval/smoke``);
  * FILTER the verified golden oracle (``test/golden/facts.yaml``) down to the manifest
    source_uris — verbatim entries, never re-labelled — into ``eval/smoke/facts.yaml``.

``source_root`` / ``dest_root`` are split so tests can build into a tmp dir without touching
the repo. Both default to the repo root.

USAGE:  python -m eval.smoke.build_smoke
"""

from __future__ import annotations

import shutil
from pathlib import Path

import yaml  # type: ignore[import-untyped]

from eval.smoke.manifest import MANIFEST, SOURCE_URIS

ROOT = Path(__file__).resolve().parents[2]

_FACT_HEADER = (
    "# AUTO-GENERATED by eval/smoke/build_smoke.py (SP_015) — a curated ONE-PER-TYPE\n"
    "# subset of test/golden/facts.yaml. Verbatim entries, filtered to the smoke corpus;\n"
    "# do not hand-edit — re-run the builder.\n"
)


def _load(path: Path) -> dict:
    with path.open(encoding="utf-8") as fh:
        return yaml.safe_load(fh)


def _dump(obj: object, path: Path, header: str) -> None:
    with path.open("w", encoding="utf-8") as fh:
        fh.write(header)
        yaml.safe_dump(obj, fh, sort_keys=False, allow_unicode=True, width=100)


def build(source_root: Path = ROOT, dest_root: Path = ROOT) -> dict:
    """Copy the manifest docs + filter the golden oracle. Returns a small summary.

    Raises ``SystemExit`` if any manifest doc is missing from ``source_root`` — a missing
    doc must fail loudly, never silently shrink the proving set.
    """
    source_root = Path(source_root)
    dest_root = Path(dest_root)
    subset = set(SOURCE_URIS)
    smoke_dir = dest_root / "eval" / "smoke"
    smoke_data = smoke_dir / "data"

    # 1) copy docs, preserving the data/-relative subpath so source_uri + globs match.
    if smoke_data.exists():
        shutil.rmtree(smoke_data)
    for _arch, uri, _why in MANIFEST:
        src = source_root / uri
        if not src.exists():
            raise SystemExit(f"manifest doc missing from corpus: {uri}")
        dst = smoke_dir / uri  # <dest>/eval/smoke/data/<subpath>
        dst.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(src, dst)

    # 2) filter golden facts by source_uri (verbatim — no re-labelling).
    golden = _load(source_root / "test" / "golden" / "facts.yaml")
    kept_facts = [f for f in golden.get("facts", []) if f.get("source_uri") in subset]
    kept_ids = {f["id"] for f in kept_facts}

    # contradictions: keep when claim_a survives; trim corroborators to the subset.
    kept_contras = []
    for c in golden.get("contradictions", []):
        if c.get("claim_a") in kept_ids:
            c = dict(c)
            c["corroborating_sources"] = [
                s for s in c.get("corroborating_sources", []) if s in subset
            ]
            kept_contras.append(c)

    smoke_dir.mkdir(parents=True, exist_ok=True)
    _dump({"facts": kept_facts, "contradictions": kept_contras}, smoke_dir / "facts.yaml", _FACT_HEADER)

    covered = {f["source_uri"] for f in kept_facts}
    return {
        "docs": len(MANIFEST),
        "facts": len(kept_facts),
        "contradictions": len(kept_contras),
        "uncovered": sorted(u for u in SOURCE_URIS if u not in covered),
    }


def main() -> int:
    s = build()
    print(f"smoke corpus: {s['docs']} docs -> eval/smoke/data")
    print(f"facts kept:   {s['facts']}; contradictions: {s['contradictions']}")
    if s["uncovered"]:
        print(f"WARNING uncovered (no golden fact): {', '.join(s['uncovered'])}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
